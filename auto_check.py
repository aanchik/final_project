# Прогоняет набор контрольных вопросов через RAG, сверяет ответы с ключевыми словами
# и генерирует отчет об эффективности системы (Accuracy).
import time
import re
from rag_simple import RobustRAG 

# Список вопросов и ожидаемых ключевых слов для обновленной модели
TESTS = [
    ("Пассажир утверждает, что у него с собой всего 7 килограммов охотничьих патронов в заводской упаковке. Можем пропустить?", ["5 кг", "не более 5", "запрещено"]),
    ("Можно ли принять к перевозке патроны, если они лежат в одном кейсе вместе с пистолетом?", ["отдельно", "размещаются отдельно", "не допускается в одном"]),
    ("На рамке пассажир заявляет, что он действующий дипломат и у него иммунитет. Обязан ли он проходить досмотр на общих основаниях?", ["не подлежат", "освобождаются", "не досматриваются"]),
    ("Пассажир сдал багаж, ушел в стерильную зону, но на интроскопе в его сумке заметили контур ножа. Пассажира найти не можем. Имеем право вскрыть сумку без него?", ["допускается", "в присутствии двух свидетелей", "составлением акта"]),
    ("К какому типу досмотра прибегнуть, если поступило анонимное сообщение о возможной угрозе на конкретном рейсе, хотя первичный контроль все пассажиры уже успешно прошли?", ["повторный", "повторного досмотра"]),
    ("Летит трансферный пассажир с пересадкой. Оружие перегружается автоматически службами аэропорта или нам обязательно нужно вызывать пассажира для повторного оформления в аэропорту стыковки?", ["без выдачи", "до конечного", "трансфер"]),
    ("Военный караул сопровождает секретный груз и требует пропустить их в салон самолета с личным боевым оружием. Каковы наши действия?", ["сдаче", "изолированном", "запрещено в салон"]),
    ("Пассажир принес на регистрацию сигнальный пистолет и пачку капсюлей к нему. Оформляем как полноценное оружие по акту или это приравнивается к бытовым спецсредствам и можно везти в обычном багаже?", ["по акту", "приемки оружия", "передачей на борт"]),
    ("Гражданин пытается пронести в кармане патрон. Какую сумму штрафа в рублях мы должны выписать ему прямо на месте согласно Приказу № 34?", ["не устанавливает", "нет информации", "отсутствует"]),
    ("Разрешено ли сотрудникам САБ применять электрошокеры при задержании нарушителя в зоне досмотра согласно тексту предоставленных регламентов?", ["не содержится", "отсутствует", "не регламентирует"])
]

def check_answer(answer, keywords):
    low = answer.lower()
    for kw in keywords:
        if kw.lower() in low:
            return True
    return False

def main():
    rag = RobustRAG()
    results = []
    total = len(TESTS)
    
    for i, (q, kws) in enumerate(TESTS, 1):
        print(f"{i}. {q[:60]}...")
        start = time.time()
        
        # Получаем ответ напрямую, без конструкций try/except
        ans = rag.ask(q)
        
        elapsed = time.time() - start
        ok = check_answer(ans, kws)
        results.append((q, ans[:200], ok, elapsed))
        print(f"   {'✅' if ok else '❌'} ({elapsed:.1f}s) -> {ans[:100]}")
        
    correct = sum(1 for _, _, ok, _ in results if ok)
    accuracy_text = f"\nAccuracy: {correct}/{total} = {correct/total:.2%}"
    print(accuracy_text)
    
    # Сохраняем подробный отчет
    with open("auto_report.txt", "w", encoding="utf-8") as f:
        f.write(accuracy_text + "\n\n")
        for q, ans, ok, t in results:
            f.write(f"Вопрос: {q}\nОтвет: {ans}\nСтатус: {'✅' if ok else '❌'}\nВремя: {t:.1f}s\n\n")
            
    print("Отчёт сохранён в auto_report.txt")

if __name__ == "__main__":
    main()